public class Student {
    String name;
    int dateOfBirth;
    int phoneNumber;
    String nationality;

    public Student() {
    }

    public Student(String name, int dateOfBirth, int phoneNumber, String nationaly) {
        this.name = name;
        this.dateOfBirth = dateOfBirth;
        this.phoneNumber = phoneNumber;
        this.nationality = nationaly;
    }
}
